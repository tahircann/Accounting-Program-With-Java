    private void SilButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SilButonActionPerformed
        //çalışan silmek için gerekli özel hibernate fonksiyonları ile veri tabanına veri eklenmesi
        Transaction transaction = null;
        try ( Session session = HibernateUtil.getSessionFactory().openSession()) {
            transaction = session.beginTransaction();
            Calisan calisanlar = new Calisan();

            calisanlar.setCalisanNo(Integer.parseInt(calisanSiltxt.getText()));
            if (calisanlar.getCalisanNo() == Integer.parseInt(calisanSiltxt.getText())) {
                session.delete(calisanlar);
                transaction.commit();

            } else {
                JOptionPane.showMessageDialog(null, "Çalışan Mevcut değil");
            }
            transaction.commit();
        } catch (Exception e) {
            if (transaction != null) {
                transaction.rollback();
            }
            e.printStackTrace();
        }
    }//GEN-LAST:event_SilButonActionPerformed

    private void duzenleButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_duzenleButonActionPerformed
        //kullanıcının jTable dan mouse ile seçtiği değerin güncellenmesi
        DefaultTableModel model = (DefaultTableModel) calisanTablo.getModel();
        int index = calisanTablo.getSelectedRow();
        Transaction transaction = null;
        try ( Session session = HibernateUtil.getSessionFactory().openSession()) {
            transaction = session.beginTransaction();

            Calisan calisanlar = (Calisan) session.get(Calisan.class, index);

            String YeniAd = JOptionPane.showInputDialog(null, "Yeni Çalışan Adı:", calisanlar.getCalisanAdi());

            String YeniSad = JOptionPane.showInputDialog(null, "Yeni Çalışan Soyadı:", calisanlar.getCalisanSadi());

            model.setValueAt(YeniAd, index, 1);
            model.setValueAt(YeniSad, index, 2);
            calisanlar.setCalisanAdi(YeniAd);
            calisanlar.setCalisanSadi(YeniSad);

            transaction.commit();

            JOptionPane.showMessageDialog(null, "Çalışan Başarıyla Değiştirildi");
        } catch (Exception e) {
            if (transaction != null) {
                transaction.rollback();
            }
            e.printStackTrace();

        }


    }//GEN-LAST:event_duzenleButonActionPerformed
    public void dinamikAra(String ara) {
        //arama çubuğu içim gerekli jTable da değer arayan method
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        calisanTablo.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(ara));

    }
