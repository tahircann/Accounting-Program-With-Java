    public ArrayList<Calisan> getCalisanlarArrayList() {
        // Listelerin bastırılması için gerekli array sınıfı
        Session session = HibernateUtil.getSessionFactory().openSession();
        Transaction transaction = null;
        ArrayList<Calisan> calisanlar = null;

        try {
            transaction = session.beginTransaction();
            calisanlar = (ArrayList<Calisan>) session.createQuery("from Calisan", Calisan.class).list();

            transaction.commit();
        } catch (Exception e) {
            if (transaction != null) {
                transaction.rollback();
            }
            e.printStackTrace();
        } finally {
            session.close();
        }
        return calisanlar;

    }

