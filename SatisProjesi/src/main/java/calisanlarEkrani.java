    private void calisanListeleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calisanListeleActionPerformed
        // Arrraylisti döndüren ürün listeleme butonu
        try {
            ArrayList<Calisan> calisanlar = getCalisanlarArrayList();

            while (model.getRowCount() > 0) {
                model.removeRow(0);
            }

            for (Calisan calisan : calisanlar) {
                Object[] row = {calisan.getCalisanNo(), calisan.getCalisanAdi(),
                    calisan.getCalisanSadi()};
                model.addRow(row);
            }

        } catch (Exception e) {

            e.printStackTrace();
        }
    }//GEN-LAST:event_calisanListeleActionPerformed

    private void ekleButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleButonActionPerformed
        // çalışan ekleme için gerekli özel hibernate fonksiyonları ile veri tabanına veri eklenmesi
        Session session = HibernateUtil.getSessionFactory().openSession();
        Transaction transaction = null;
        transaction = session.beginTransaction();

        Calisan calisan = new Calisan();
        calisan.getCalisanNo();
        calisan.setCalisanAdi(calisanAditext.getText());
        calisan.setCalisanSadi(calisanSaditxt.getText());

        session.save(calisan);
        transaction.commit();
        session.close();

    }//GEN-LAST:event_ekleButonActionPerformed

